int	**atoi(char *str)
{
	int i;
	int j;
	int **tab;

	i = 0;
	j = 0;
	while (str[i])
	{
		j = 0;
		while (j < 4)
		{
			tab[i][j] = tab[i][j] * 10 + str[i] - '0';
			j++;
		}
		i += 2;
	}
	return (tab);
}
